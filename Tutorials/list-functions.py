
# List Functions

# 1)Append - Adding an item to the List

fruits =["Mango", "Apple","Banana","Peach"]
fruits.append("Orange")
print(fruits)

# 2)len - To know the length of the List

print(len(fruits))

# 3)Insert - To add an item at a specific index number.

fruits.insert(2, "Jackfruit")
print(fruits)

# 4)Index - to know the index number of an item in the list.

print(fruits.index("Apple"))

